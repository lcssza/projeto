function pedir() {
  pedido = prompt("O que deseja pedir?");
  switch (pedido) {
    case "Sunny Salmon":
      alert("Voce pediu um Sunny Salmon!");
      break;
    case "Fire Salmon":
      alert("Voce pediu um Fire Salmon!");
      break;
    case "Roast Beef":
      alert("Voce pediu um Roast Beef!");
      break;
    case "Chicken":
      alert("Voce pediu um Chicken!");
      break;
    case "Vegan":
      alert("Voce pediu um Vegan!");
      break;
    case "Spicy Tuna":
      alert("Voce pediu um Spicy Tuna!");
      break;
    case "Seakiss":
      alert("Voce Seakiss!");
      break;
  }
}
